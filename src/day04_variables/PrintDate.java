package day04_variables;

public class PrintDate {
    public static void main(String[] args) {
           /*
        Task:
            declare and assign 3 variables
            the month number, the day number, and year number

            print the date in the following format:
                month/day/year

     */
        int month = 9;
        int day = 6; // if you start ur number with ZERO (o) it will not show it in output
        int year = 2023;    // or int month, day year

        System.out.println(month + "/" + day + "/" + year);
        System.out.println(month + "." + day + "." + year);
        System.out.println(month + "-" + day + "-" + year);
        System.out.println(month + "|" + day + "|" + year);












    }
}
