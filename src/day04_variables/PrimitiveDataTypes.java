package day04_variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        byte age = 50; // I declared a variable called AGE which has int data type and number  50 is assigned to it.
        System.out.println("50"); //Hardcoded -- means you have to change it manually

        System.out.println("age"); // here age is not variable. It is just a word that we are printing

        System.out.println(age); //Dynamic - we are using age variable to make our code DYNAMIC

        byte age2 = 60;

        System.out.println(age2);

        short year;
        year = 2023;

        int addressNumber = 256541223;

        long bigNumber;
        bigNumber = 458769214644l;









    }
}
